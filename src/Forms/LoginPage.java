public void Verify()
{
 try
{
  
         String user = username.getText();
         String pass = password.getText();
         conn = DriverManager.getConnection("jdbc:mysql://localhost/meisdb", "root","root");                 
         Statement st = (Statement) conn.createStatement();
         ResultSet res = st.executeQuery("SELECT * FROM user_table WHERE username ='"+user+"' and password ='"+pass+"' "); 
        
         if(res.next())
         {

         String usertopass = res.getString("username");
         JOptionPane.showMessageDialog(null, "Login Successful: "+usertopass);
         this.setVisible(false);
         new Main_UI(usertopass).setVisible(true);

         }
         else
         {
         JOptionPane.showMessageDialog(null, "Username or Password Incorrect");
         }
}
    
catch(Exception e)
{
JOptionPane.showMessageDialog(null, e);
}

}
